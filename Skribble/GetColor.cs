    public static double CalculateEuclideanDistance(Color color1, ColorPalette color2){
        int rDiff = color1.R - color2.R;
        int gDiff = color1.G - color2.G;
        int bDiff = color1.B - color2.B;
        return Math.Sqrt(rDiff * rDiff + gDiff * gDiff + bDiff * bDiff);
    }


    public static int CalculateManhattanDistance(Color color1, ColorPalette color2){
        var rDiff = Math.Abs(color1.R - color2.R);
        var gDiff = Math.Abs(color1.G - color2.G);
        var bDiff = Math.Abs(color1.B - color2.B);
        return rDiff + gDiff + bDiff;
    }

    public string FindColour(Color color){

        ColorPalette closestColor = null;
        var minDistance = double.MaxValue;

        var ColorPalette = GetAllColorPalette().ToList();

        if (index == 1){
            ColorPalette = GetAllColorPalette()
                           .Where((item, index) => index % 2 == 0).ToList();
        }

        foreach (ColorPalette palette in ColorPalette){
            double distance = CalculateEuclideanDistance(color, palette);
            if (distance < minDistance){
                minDistance = distance;
                closestColor = palette;
            }
        }


        return closestColor?.Name;
    }

